/**
 * slugify — lowercase + NFD normalize + strip accents + spaces→hyphens
 * Identique à la version utilisée dans le script sync.js
 */
export function slugify(text: string): string {
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9\s-]/g, "")
    .trim()
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-");
}
